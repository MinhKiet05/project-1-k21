import { useState, useEffect } from 'react'
import { supabase } from '../lib/supabase'

/**
 * ðŸ‘¥ Hook Ä‘á»ƒ quáº£n lÃ½ users tá»« Supabase
 */
export function useUsers() {
  const [users, setUsers] = useState([])
  const [loading, setLoading] = useState(true)
  const [error, setError] = useState(null)

  // Láº¥y danh sÃ¡ch users
  const fetchUsers = async () => {
    try {
      setLoading(true)
      const { data, error } = await supabase
        .from('profiles')
        .select('*')
        .order('created_at', { ascending: false })

      if (error) throw error
      setUsers(data || [])
    } catch (err) {
      setError(err.message)
    } finally {
      setLoading(false)
    }
  }

  // Cáº­p nháº­t role user (roles lÃ  array)
  const updateUserRole = async (userId, newRole) => {
    try {
      // Update vá»›i array format
      const roleArray = [newRole]
      const { error } = await supabase
        .from('profiles')
        .update({ roles: roleArray })
        .eq('id', userId)

      if (error) throw error
      
      // Cáº­p nháº­t local state
      setUsers(users.map(user => 
        user.id === userId ? { ...user, roles: roleArray } : user
      ))
      
      return { success: true }
    } catch (err) {
      return { success: false, error: err.message }
    }
  }

  useEffect(() => {
    fetchUsers()
  }, [])

  return {
    users,
    loading,
    error,
    refetch: fetchUsers,
    updateUserRole
  }
}